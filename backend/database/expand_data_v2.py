def get_emotion_id(cursor, spectrum: str, intensity: int) -> int | None:
    cursor.execute("SELECT id FROM emotion_spectrum WHERE spectrum=? AND intensity=?", (spectrum, intensity))
    r = cursor.fetchone()
    return r[0] if r else None


